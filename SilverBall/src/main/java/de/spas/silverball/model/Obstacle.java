public class Obstacle {
    private String type;
    private String texture;
    private int x;
    private int y;
    private int w;
    private int h;

    public String getType() {
        return type;
    }

    public String getTexture() {
        return texture;
    }


    public int getH() {
        return h;
    }

    public int getW() {
        return w;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
